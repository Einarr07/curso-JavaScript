// Inicializamos la variable numero en 1
let numero = 1;

// Bucle do...while que ejecuta al menos una vez
do {
    // Imprimimos el valor actual de numero en el documento
    document.write(`Número: ${numero} <br>`);

    // Incrementamos el valor de numero en 1
    numero++;
} while (numero <= 6); // El bucle continúa mientras numero sea menor o igual a 6
